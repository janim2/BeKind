package com.example.bekind.Models;

public class OrgModel {
    String OrgId;
    String Orgname;
    String Orgimage;
    String OrgInfo;
    String OrgEmail;
    String OrgPhone;
    String OrgAddress;


    public void setOrgId(String OrgId){this.OrgId = OrgId;}
    public String getOrgId(){return OrgId;}

    public void setOrgname(String Orgname){this.Orgname = Orgname;}
    public String getOrgname(){return Orgname;}

    public void setOrgimage(String Orgimage){this.Orgimage = Orgimage;}
    public String getOrgimage(){return Orgimage;}

    public void setOrgInfo(String OrgInfo){this.OrgInfo = OrgInfo;}
    public String getOrgInfo(){return OrgInfo;}

    public void setOrgEmail(String OrgEmail){this.OrgEmail = OrgEmail;}
    public String getOrgEmail(){return OrgEmail;}

    public void setOrgPhone(String OrgPhone){this.OrgPhone = OrgPhone;}
    public String getOrgPhone(){return OrgPhone;}

    public void setOrgAddress(String OrgAddress){this.OrgAddress = OrgAddress;}
    public String getOrgAddress(){return OrgAddress;}


}

